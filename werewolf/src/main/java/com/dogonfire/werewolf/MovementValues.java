package com.dogonfire.werewolf;

public class MovementValues
{
    int x;
    int y;
    int z;
    int yaw;
    int pitch;
    
    public MovementValues(final int x, final int y, final int z, final int yaw, final int pitch) {
        super();
        this.x = x;
        this.y = y;
        this.z = z;
        this.yaw = yaw;
        this.pitch = pitch;
    }
}
